<h2>Placing a Bid or Creating a Buy Now Request</h2>
<p>
Bidding starts when the vehicle is assigned a sale date and continues until one hour (cut-off)
before the ‘Live Auction’. You can submit the maximum price you are willing to pay for the
vehicle on the vehicle page. You can retract, lower, make it higher, or cancel the bid any time
before the cut-off.
</p>
<p>
We also allow you to place requests for the lots with ‘Buy Now’ options. You can create a
‘Buy Now’ request from the lot page until one hour (cut off) before the ‘Live Auction’. We will
place a request at the auction site and try to negotiate the purchase. We do not guarantee that
your ‘Buy Now’ request will be fulfilled as this depends on the final decision of the seller on
the auction site. You cannot change\cancel your ‘Buy Now’ request after its creation since it
will be processed immediately by the manager (as soon as you created it).
</p>
<p>
You cannot change your bid after the cut off. Beyond the cut off, Globalmotors registers your
‘Bid’ at the auction site automatically as a proxy bid, and we are unable to retract, lower or
cancel the bid at the auction’s website. If an auction ends with the ‘User’ as a high bidder,
the ‘User’ will be obliged to either purchase the vehicle or forfeit his/her security deposit.
All bids are final.
</p>
<p>
If the vehicle is won ‘On Approval’ or ‘Minimum Bid’, we will negotiate the final price on your
behalf with the seller. We may contact the bidder if the negotiated price goes over the maximum
bid amount. We reserve the right to increase the bidding amount up to your maximum bid ‘On
Approval’ and ‘Minimum Bid’ auctions without contacting you first. If you decide to stay at
your winning bid - you need to contact the Company within one (1) hour after the ‘On Approval’
purchase.
</p>
<p>
During the auction, the Copart bidding platform VB3 will bid one bid increment at a time until
your maximum bid over the preliminary bidding closed price. Please review Copart’s terms for
detailed information.
